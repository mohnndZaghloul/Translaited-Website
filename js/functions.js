//seting lang in loacal storage
var lang = localStorage.getItem('lang');
const en = document.getElementById('en');
const ar = document.getElementById('ar');
en.addEventListener('click',()=>{
    localStorage.setItem("lang","en");
    location.reload();
})
ar.addEventListener('click',()=>{
    localStorage.setItem("lang","ar");
    location.reload();
})
//changing text in page 
const elements = document.querySelectorAll("[data-tran]");
elements.forEach((element)=>{
    const translationsKey = element.getAttribute('data-tran');
    element.textContent = translations[lang][translationsKey];
    if(lang == 'en'){
        document.body.style.direction ="ltf";
    }else{
        document.body.style.direction ="rtl";        
    }
})